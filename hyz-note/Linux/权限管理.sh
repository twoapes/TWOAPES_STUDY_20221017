# 在Linux中,权限是通过文件或目录的属性来控制的。以下是Linux中常用的权限管理:

# 1. 文件权限:文件权限用于控制谁可以读取、写入或执行文件。在Linux中,每个文件都有三种类型的权限:用户权限、组权限和其他用户权限。用户权限包括读、写和执行；组权限和其他用户权限包括读和执行。

# 2. 目录权限:目录权限用于控制用户是否可以进入目录、列出目录中的文件或者在目录中创建、删除文件。在Linux中,每个目录都有三种类型的权限:用户权限、组权限和其他用户权限。用户权限包括读、写和执行；组权限和其他用户权限包括读和执行。

# 3. chmod命令:chmod命令用于更改文件或目录的权限。它允许用户设置用户、组和其他用户的权限,以及设置特殊权限如suid和sgid。

# 4. chown命令:chown命令用于更改文件或目录的所有者和所属组。它允许用户将文件或目录转移给其他用户或组。

# 5. umask命令:umask命令用于设置新创建文件或目录的默认权限。它允许用户屏蔽某些权限或者将权限添加到默认权限中。

# 6. sudo命令:sudo命令用于在Linux中以超级用户权限运行命令。它允许用户执行需要特权的操作,如安装软件或更改系统设置。

# 7. su命令:su命令用于在Linux中切换用户。它允许用户从普通用户切换到超级用户,以便执行需要特权的操作。

在Linux中,文件和目录的权限由10个字符表示,它们分别表示文件类型、用户权限、用户组权限和其他用户权限。以下是所有授权的demo及其解释:

rw-------:该文件或目录的所有者具有读写权限,其他用户没有任何权限。数字表示为600。
rw-r--r--:该文件或目录的所有者具有读写权限,用户组和其他用户只有读权限。数字表示为644。
rw-rw-rw-:该文件或目录的所有者、用户组和其他用户都具有读写权限。数字表示为666。
rwx------:该文件或目录的所有者具有读、写和执行权限,其他用户没有任何权限。数字表示为700。
rwxr-xr-x:该文件或目录的所有者具有读、写和执行权限,用户组和其他用户只有执行权限。数字表示为755。
rwxrwxrwx:该文件或目录的所有者、用户组和其他用户都具有读写和执行权限。数字表示为777。
r--r--r--:该文件或目录的所有者、用户组和其他用户只有读权限。数字表示为444。
-wx-wx-wx:该文件或目录的所有者没有任何权限,用户组和其他用户具有写和执行权限。数字表示为223。
drwx------:该目录的所有者具有读、写和执行权限,其他用户没有任何权限。数字表示为700。
drwxr-xr-x:该目录的所有者具有读、写和执行权限,用户组和其他用户只有执行权限。数字表示为755。

其中,数字表示可以通过将每个权限转换为二进制数并将其连接起来来计算。例如,rw-表示二进制数110,而r--表示二进制数100,所以rw-r--r--对应的数字是110100100,将其转换为十进制数得到644。
其中,第一个字符表示文件类型,如d表示目录,-表示普通文件,l表示符号链接,s表示套接字等等。其余9个字符分别表示用户权限、用户组权限和其他用户权限,每三个字符一组。其中,r表示读取权限,w表示写入权限,x表示执行权限,-表示没有该权限。

